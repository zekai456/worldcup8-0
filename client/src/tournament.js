export function pickChampionOpponent(matchday = 1, { seed = Math.random() } = {}) {
  return pickStrongOpponent(matchday, { seed });
}

export function pickStrongOpponent(matchday = 1, { seed = Math.random(), exclude = [] } = {}) {
  const round = Number(matchday) || 1;
  const windows = {
    1: { minStrength: 88, tiers: ['contender', 'elite', 'legendary'], eras: [1954, 1998] },
    2: { minStrength: 90, tiers: ['elite', 'legendary'], eras: [1970, 2014] },
    3: { minStrength: 90, tiers: ['elite', 'legendary'], eras: [1982, 2026] },
    4: { minStrength: 90, tiers: ['elite', 'legendary'], eras: [1990, 2026] },
    5: { minStrength: 91, tiers: ['elite', 'legendary'], eras: [1998, 2026] },
    6: { minStrength: 92, tiers: ['elite', 'legendary'], eras: [2002, 2026] },
    7: { minStrength: 93, tiers: ['elite', 'legendary'], eras: [2006, 2026] },
    8: { minStrength: 94, tiers: ['legendary'], eras: [2010, 2026] },
  };
  const rule = windows[round] || windows[8];
  const excluded = new Set(exclude.map(opponentKey));
  const excludedCountries = new Set(exclude.map((team) => team?.country).filter(Boolean));
  let pool = HISTORIC_STRONG_TEAMS.filter((team) => {
    const inEra = team.year >= rule.eras[0] && team.year <= rule.eras[1];
    return (
      inEra &&
      team.strength >= rule.minStrength &&
      rule.tiers.includes(team.tier) &&
      !excluded.has(opponentKey(team)) &&
      !excludedCountries.has(team.country)
    );
  });
  if (!pool.length) {
    pool = HISTORIC_STRONG_TEAMS.filter((team) => !excluded.has(opponentKey(team)) && !excludedCountries.has(team.country));
  }
  if (!pool.length) pool = HISTORIC_STRONG_TEAMS.filter((team) => !excluded.has(opponentKey(team)));
  if (!pool.length) pool = HISTORIC_STRONG_TEAMS;
  const normalizedSeed = Number.isFinite(Number(seed)) ? Math.abs(Number(seed)) % 1 : Math.random();
  const picked = pool[Math.min(pool.length - 1, Math.floor(normalizedSeed * pool.length))] || HISTORIC_STRONG_TEAMS.at(-1);
  return {
    ...picked,
    round,
    label: `${picked.year} ${picked.country}`,
    champion: !!picked.champion,
  };
}

function opponentKey(team) {
  return `${team?.year || ''}-${team?.country || ''}`;
}
